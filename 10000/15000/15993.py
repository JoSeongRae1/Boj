# 1 = 1 (1) (1, 0)
# 2 = 1+1, 2 (2) (1, 1)
# 3 = 1+1+1, 1+2, 2+1, 3 (4) (2, 2)
# 4 = 1+1+1+1, 1+1+2, 1+2+1, 2+1+1, 2+2, 1+3, 3+1 (7) (3, 4)
# 5 = 1+1+1+1+1, 1+1+1+2, 1+1+2+1, 1+2+1+1, 2+1+1+1, 1+1+3, 1+3+1, 3+1+1, 2+2+1, 2+1+2, 1+2+2, 3+2, 2+3 (13) (7, 6)

#  1 : 1, 0
#  2 : 1, 1
#  4 : 2, 2
#  7 : 3, 4
# 13 : 7, 6

m = 1000000009

dp = [(1, 0), (1, 1), (2, 2), (3, 4), (7, 6)]
k = 5
for i in range(int(input())):
    N = int(input())
    while len(dp) < N:
        a = dp[k-1]
        b = dp[k-2]
        c = dp[k-3]
        dp.append(((a[1]+b[1]+c[1])%m, (a[0]+b[0]+c[0])%m))
        k += 1
    print(*dp[N-1])